package io.github.texport.superkassa.jvm.shared.strings.api

/**
 * Сервис для сопоставления [ErrorKey] с его локализованными переводами [TrilingualString].
 * Позволяет отделить данные локализации (строки перевода) от бизнес-логики.
 *
 * Потокобезопасность: Реализации ОБЯЗАНЫ быть полностью потокобезопасными.
 */
interface ErrorResolver {
    /**
     * Разрешает указанный ключ ошибки в трехъязычное локализованное сообщение.
     *
     * @param key Уникальный идентификатор ключа ошибки.
     * @return Разрешенная строка [TrilingualString] или дефолтный фолбек, если ключ не зарегистрирован.
     */
    fun resolve(key: ErrorKey): TrilingualString
}
